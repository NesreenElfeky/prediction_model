from __future__ import annotations

import json
from pathlib import Path

import matplotlib.pyplot as plt
from matplotlib.backends.backend_pdf import PdfPages
import pandas as pd

from ml_pipeline.feature_engineering.builder import FEATURE_EXPLANATIONS


def write_reports(context: dict, report_dir: str | Path, artifact_dir: str | Path) -> None:
    report_dir = Path(report_dir)
    artifact_dir = Path(artifact_dir)
    report_dir.mkdir(parents=True, exist_ok=True)
    leaderboard = context["leaderboard"]
    metrics = context["metrics"]
    selected = context["selected_features"]

    md = _markdown(context, leaderboard, metrics, selected)
    (report_dir / "best_model_report.md").write_text(md, encoding="utf-8")
    (artifact_dir / "best_model_report.md").write_text(md, encoding="utf-8")
    (report_dir / "model_report.html").write_text(_html(md, context["plots"], context["shap_paths"]), encoding="utf-8")
    _pdf(report_dir / "model_report.pdf", context)


def _markdown(context: dict, leaderboard: pd.DataFrame, metrics: dict, selected: list[str]) -> str:
    lines = [
        "# Cyber Financial Loss Prediction ML Report",
        "",
        "## Project Overview",
        "This pipeline predicts `financial_loss_usd` from asset-level cybersecurity signals generated by the project. Financial component columns are excluded from model inputs to prevent target leakage.",
        "",
        "## Dataset Statistics",
        f"- Rows: {context['profile']['shape']['rows']}",
        f"- Columns before feature engineering: {context['profile']['shape']['columns']}",
        f"- Selected model features: {len(selected)}",
        "",
        "## Feature Engineering Summary",
    ]
    for feature in selected:
        explanation = FEATURE_EXPLANATIONS.get(feature, "Existing upstream cybersecurity signal retained after statistical feature selection.")
        lines.append(f"- `{feature}`: {explanation}")
    lines.extend(
        [
            "",
            "## Model Comparison",
            _leaderboard_markdown(leaderboard),
            "",
            "## Best Model",
            f"- Selected model: **{context['best_model_name']}**",
            f"- R2: {metrics['r2']:.4f}",
            f"- RMSE: {metrics['rmse']:.2f}",
            f"- MAE: {metrics['mae']:.2f}",
            f"- MAPE: {metrics['mape']:.2f}%",
            f"- Median Absolute Error: {metrics['median_absolute_error']:.2f}",
            f"- Explained Variance: {metrics['explained_variance']:.4f}",
            "",
            "## Business Interpretation",
            "The model prioritizes assets where technical severity, exploit likelihood, KEV evidence, and exposure concentrate into higher expected financial loss.",
            "",
            "## Cybersecurity Interpretation",
            "Features such as EPSS, CVSS, vulnerability density, KEV strength, and attack-surface indicators represent exploitability, exposure, and operational risk.",
            "",
            "## Financial Interpretation",
            "Predictions are portfolio prioritization estimates derived from generated project loss labels, not accounting statements. Use them to rank risk and focus remediation.",
            "",
            "## Uncertainty",
            "Prediction intervals are estimated from holdout residual quantiles and saved with the training artifacts.",
        ]
    )
    return "\n".join(lines)


def _leaderboard_markdown(leaderboard: pd.DataFrame) -> str:
    cols = ["model", "holdout_r2", "holdout_rmse", "holdout_mae", "cv_rmse_mean", "cv_r2_mean"]
    existing = [c for c in cols if c in leaderboard.columns]
    table = leaderboard[existing].copy()
    for col in table.select_dtypes(include="number").columns:
        table[col] = table[col].map(lambda value: "" if pd.isna(value) else f"{value:.4f}")
    header = "| " + " | ".join(existing) + " |"
    sep = "| " + " | ".join(["---"] * len(existing)) + " |"
    rows = ["| " + " | ".join(str(value) for value in row) + " |" for row in table.to_numpy()]
    return "\n".join([header, sep, *rows])


def _html(markdown_text: str, plots: dict, shap_paths: dict) -> str:
    body = markdown_text.replace("&", "&amp;").replace("<", "&lt;").replace(">", "&gt;")
    images = {**plots, **{k: v for k, v in shap_paths.items() if v.endswith(".png")}}
    img_html = "\n".join(f"<h3>{name}</h3><img src='{Path(path).as_posix()}' />" for name, path in images.items())
    return f"""<!doctype html>
<html><head><meta charset="utf-8"><title>Cyber Financial Loss ML Report</title>
<style>body{{font-family:Arial,sans-serif;margin:40px;line-height:1.5;color:#172033}} pre{{white-space:pre-wrap}} img{{max-width:100%;border:1px solid #ddd;margin-bottom:24px}}</style>
</head><body><pre>{body}</pre>{img_html}</body></html>"""


def _pdf(path: Path, context: dict) -> None:
    with PdfPages(path) as pdf:
        fig = plt.figure(figsize=(8.5, 11))
        fig.text(0.08, 0.92, "Cyber Financial Loss Prediction", fontsize=18, weight="bold")
        fig.text(0.08, 0.86, f"Best model: {context['best_model_name']}", fontsize=12)
        fig.text(0.08, 0.82, json.dumps(context["metrics"], indent=2), fontsize=9, family="monospace")
        pdf.savefig(fig)
        plt.close(fig)
        for plot in context["plots"].values():
            if plot and Path(plot).exists():
                image = plt.imread(plot)
                fig, ax = plt.subplots(figsize=(10, 7))
                ax.imshow(image)
                ax.axis("off")
                pdf.savefig(fig)
                plt.close(fig)
